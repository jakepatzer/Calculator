/**
 * Stores a pair of values
 */
public class Pair {

    public int first;
    public int second;

    /**
     * Constructor
     *
     * @param val1 The first value to store
     * @param val2 The second value to store
     */
    public Pair(int val1, int val2) {
        first = val1;
        second = val2;
    }

}
